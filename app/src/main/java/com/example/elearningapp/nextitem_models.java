package com.example.elearningapp;

public class nextitem_models {

    int videos;
    String title1;

    public nextitem_models(int videos, String title1) {
        this.videos = videos;
        this.title1 = title1;
    }

    public int getVideos() {
        return videos;
    }

    public void setVideos(int videos) {
        this.videos = videos;
    }

    public String getTitle1() {
        return title1;
    }

    public void setTitle1(String title1) {
        this.title1 = title1;
    }
}
